from math import sin, cos, tan, radians
angulo = float(input('Digite o 창ngulo desejado: '))
print(f'O 창ngulo de {angulo} tem SENO de {sin(radians(angulo)) :.2f}\n'
      f'O 창ngulo de {angulo} tem COSENO de {cos(radians(angulo)) :.2f}\n'
      f'O 창ngulo de {angulo} tem TANGENTE de {tan(radians(angulo)) :.2f}')
